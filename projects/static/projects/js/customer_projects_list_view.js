// Selection Window Project POP UP CODE
 
 
function openPopup_selection_project() {
    document.getElementById("selection_window_project").style.display = "block";
    document.getElementById("selection_window_project_overlay").style.display = "block";
 
}
 
function closePopup_selection_project() {
    document.getElementById("selection_window_project").style.display = "none";
    document.getElementById("selection_window_project_overlay").style.display = "none";
}