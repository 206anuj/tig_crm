// Selection Window account POP UP CODE

function openPopup_selection_account() {
    document.getElementById("selection_window_account").style.display = "block";
    document.getElementById("selection_window_account_overlay").style.display = "block";

}

function closePopup_selection_account() {
    document.getElementById("selection_window_account").style.display = "none";
    document.getElementById("selection_window_account_overlay").style.display = "none";
}